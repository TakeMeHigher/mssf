"""
给定一个数组，找出数组中是否有两个数对（a，b）和（c，d），使得a+b=c+d，其中，a、b、c和d是不同的元素。如果有多个答案，打印任意一个即可。
例如给定数组：[3，4，7，10，20，9，8]，可以找到两个数对（3，8）和（4，7），使得3+8=4+7。
"""
aa = [3, 4, 7, 10, 20, 9, 8]

dic = {}

for i in aa:
    for j in aa[aa.index(i):]:
        if i+j not in dic:
            dic[i+j] = (i, j)
        else:
            print(dic[i+j], "===========", (i,j))
